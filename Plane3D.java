public class Plane3D {

    private double x;
    private double y;
    private double z;
    private double d;

    public Plane3D(double x, double y, double z, double d) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.d = d;
    }

    public double getX() {
        return this.x;
    }

    public double getY() {
        return this.y;
    }

    public double getZ() {
        return this.z;
    }

    public double getD() {
        return this.d;
    }
    
}
